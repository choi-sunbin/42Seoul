#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int loop;
	
	loop = 0;
	while (loop+1 <= size / 2)
	{
		ft_swap(&tab[loop], &tab[(size - 1) - loop]);
		loop++;
	}
}
