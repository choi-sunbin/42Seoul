#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char* str)
{
	int	loop;
	loop = 0;
	while(str[loop] != 0)
	{
		ft_putchar(str[loop++]);
	}
}
